class primitive_datatypes{
	public static void main(String args[]) {
		
		byte maxsize=127;
		byte minsize=-128;
		short maxsize1=32767;
		short minsize1=-32768;
		int max=2147483647;
		int min=-2147483648;
		long sizemax = 9223372036854775807L;
		long sizemin= -9223372036854775808L;
		
		System.out.println("byte Maximum size::"+maxsize);
		System.out.println("byte Minimum size::"+minsize);
		System.out.println("short Maximum size::"+maxsize1);
		System.out.println("short Minimum size::"+minsize1);
		System.out.println("Integer Maximum size::"+max);
		System.out.println("Integer Minimum size::"+min);
		System.out.println("Long Maximum size::   "+sizemax);
		System.out.println("Integer Maximum size::"+sizemin);
		
		
		
		
	}
}